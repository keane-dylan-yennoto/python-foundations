# Lesson 4: Types of Numbers in Python

# Integer
num1 = 10
print("Integer:", num1)

# Float
num2 = 3.14
print("Float:", num2)

# Complex
num3 = 2 + 3j
print("Complex:", num3)

# Type conversion
x = 5
y = 2.5

# Integer to float
result1 = float(x)
print("Integer to float:", result1)

# Float to integer
result2 = int(y)
print("Float to integer:", result2)

# Operations
a = 10
b = 3

# Addition
sum = a + b
print("Sum:", sum)

# Subtraction
diff = a - b
print("Difference:", diff)

# Multiplication
product = a * b
print("Product:", product)

# Division
quotient = a / b
print("Quotient:", quotient)

# Modulo
remainder = a % b
print("Remainder:", remainder)

# Exponentiation
power = a ** b
print("Power:", power)
